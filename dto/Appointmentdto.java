package dto;

import enums.LocalDateTime;
import java.time.LocalDate;

/**
 *
 * @author ACER
 */
public class Appointmentdto {
   
     private LocalDate appointment_date;
     private String appointment_type;
     private String status;
     private String notes;
     private LocalDateTime created_at;
     private String created_by;
     private LocalDateTime updated_at;
     private String updated_by;

    public Appointmentdto(LocalDate appointment_date, String appointment_type, String status, String notes, LocalDateTime created_at, String created_by, LocalDateTime updated_at, String updated_by) {
        this.appointment_date = appointment_date;
        this.appointment_type = appointment_type;
        this.status = status;
        this.notes = notes;
        this.created_at = created_at;
        this.created_by = created_by;
        this.updated_at = updated_at;
        this.updated_by = updated_by;
    }

    public Appointmentdto() {
    }

    public LocalDate getAppointment_date() {
        return appointment_date;
    }

    public void setAppointment_date(LocalDate appointment_date) {
        this.appointment_date = appointment_date;
    }

    public String getAppointment_type() {
        return appointment_type;
    }

    public void setAppointment_type(String appointment_type) {
        this.appointment_type = appointment_type;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }

    public LocalDateTime getCreated_at() {
        return created_at;
    }

    public void setCreated_at(LocalDateTime created_at) {
        this.created_at = created_at;
    }

    public String getCreated_by() {
        return created_by;
    }

    public void setCreated_by(String created_by) {
        this.created_by = created_by;
    }

    public LocalDateTime getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(LocalDateTime updated_at) {
        this.updated_at = updated_at;
    }

    public String getUpdated_by() {
        return updated_by;
    }

    public void setUpdated_by(String updated_by) {
        this.updated_by = updated_by;
    }
     
     




    
}
